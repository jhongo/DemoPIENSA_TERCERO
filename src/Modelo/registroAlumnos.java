package Modelo;

public class registroAlumnos { 
    
    private int IdEstudiantes; 
    private String nombres; 
    private String apellidos; 
    private String ci; 
    private String edad; 
    private String curso; 
    private String tiempo; 
    private String planeacion; 
    private String coordinacion; 
    private String fuerza_muscular; 
    private String sensibilidad;
    
    public registroAlumnos(){
    }
    
    public int getIdEstudiantes() {
        return IdEstudiantes;
    }

    public void setIdEstudiantes(int IdEstudiantes) {
        this.IdEstudiantes = IdEstudiantes;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getCi() {
        return ci;
    }

    public void setCi(String ci) {
        this.ci = ci;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    public String getTiempo() {
        return tiempo;
    }

    public void setTiempo(String tiempo) {
        this.tiempo = tiempo;
    }

    public String getPlaneacion() {
        return planeacion;
    }

    public void setPlaneacion(String planeacion) {
        this.planeacion = planeacion;
    }

    public String getCoordinacion() {
        return coordinacion;
    }

    public void setCoordinacion(String coordinacion) {
        this.coordinacion = coordinacion;
    }

    public String getFuerza_muscular() {
        return fuerza_muscular;
    }

    public void setFuerza_muscular(String fuerza_muscular) {
        this.fuerza_muscular = fuerza_muscular;
    }

    public String getSensibilidad() {
        return sensibilidad;
    }

    public void setSensibilidad(String sensibilidad) {
        this.sensibilidad = sensibilidad;
    }

    
    
}
